weather = 'sunny'

case weather
when 'cloudy'
  puts "A light wind breaker or a sweatshirt, will do."
when 'foggy'
  puts "Throw on a jacket and jeans to tackle this scene."
when 'rainy'
  puts "It's usually a good idea to grab an umbrella and a rain coat."
when 'sunny'
  puts "The nice shiny and warm sun, throw on some shorts and shades to face the day with!"
when 'snowy'
  puts "Bundle the heck up, it's freezing out there and you don't want to catcha cold!"
end
